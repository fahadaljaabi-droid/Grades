#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
================================================================================
برنامج تقدير الطالب (Student Grading System)
================================================================================
يقوم هذا البرنامج بتقدير أداء الطالب بناءً على درجته في الاختبار.
التقديرات المتاحة: ضعيف، جيد، جيد جداً، ممتاز
يستمر البرنامج في العمل حتى يدخل المستخدم كلمة "خروج"
================================================================================
"""

# ============================================================================
# الدوال المساعدة (Helper Functions)
# ============================================================================

def تقدير_الدرجة(الدرجة):
    """
    دالة لتحديد تقدير الطالب بناءً على الدرجة
    
    المعاملات:
        الدرجة (float): درجة الطالب، يجب أن تكون بين 0 و 100
    
    المخرجات:
        str: التقدير المناسب (ضعيف، جيد، جيد جداً، ممتاز)
    
    أمثلة:
        >>> تقدير_الدرجة(95)
        'ممتاز'
        >>> تقدير_الدرجة(80)
        'جيد جداً'
        >>> تقدير_الدرجة(65)
        'جيد'
        >>> تقدير_الدرجة(40)
        'ضعيف'
    """
    # التحقق من الدرجات من الأعلى إلى الأدنى
    if الدرجة >= 85:
        return "ممتاز"
    elif الدرجة >= 70:
        return "جيد جداً"
    elif الدرجة >= 50:
        return "جيد"
    else:
        return "ضعيف"


def عرض_قائمة_التقديرات():
    """
    دالة لعرض جدول التقديرات مع الشرح
    """
    print("\n" + "=" * 60)
    print("     🎓 نظام تقدير نتائج الطلاب 🎓")
    print("=" * 60)
    print("\n📊 جدول التقديرات المعتمدة:")
    print("-" * 60)
    print("  🏆 ممتاز    : 85 - 100 درجة")
    print("  ⭐ جيد جداً : 70 - 84 درجة")
    print("  ✅ جيد      : 50 - 69 درجة")
    print("  📖 ضعيف     : 0 - 49 درجة")
    print("-" * 60)


def عرض_التعليمات():
    """
    دالة لعرض تعليمات استخدام البرنامج
    """
    print("\n💡 تعليمات الاستخدام:")
    print("-" * 60)
    print("  1️⃣ أدخل درجة الطالب (رقم بين 0 و 100)")
    print("  2️⃣ يمكنك إدخال أرقام صحيحة أو عشرية (مثال: 85, 73.5)")
    print("  3️⃣ لإنهاء البرنامج، اكتب كلمة 'خروج'")
    print("  4️⃣ البرنامج سيعمل بشكل مستمر حتى تطلب الخروج")
    print("=" * 60)


def التحقق_من_صحة_الدرجة(النص_المدخل):
    """
    دالة للتحقق من صحة الدرجة المدخلة من المستخدم
    
    المعاملات:
        النص_المدخل (str): النص الذي أدخله المستخدم
    
    المخرجات:
        tuple: (هل_الإدخال_صحيح, قيمة_الدرجة, رسالة_الخطأ)
        - هل_الإدخال_صحيح (bool): True إذا كان الإدخال صحيحاً، False وإلا
        - قيمة_الدرجة (float): قيمة الدرجة (إذا كانت صحيحة)
        - رسالة_الخطأ (str): رسالة الخطأ المناسبة (إذا وجدت)
    """
    # إزالة المسافات الزائدة من بداية ونهاية النص
    النص_المدخل = النص_المدخل.strip()
    
    # الخطوة 1: محاولة تحويل النص إلى رقم
    try:
        الدرجة = float(النص_المدخل)
    except ValueError:
        # حدث خطأ: النص ليس رقماً صالحاً
        return False, None, "❌ خطأ: الرجاء إدخال رقم صحيح (مثل: 85, 73.5)"
    
    # الخطوة 2: التحقق من أن الدرجة في النطاق المسموح (0-100)
    if الدرجة < 0:
        return False, None, f"❌ خطأ: الدرجة {الدرجة} أقل من الصفر (الحد الأدنى هو 0)"
    
    if الدرجة > 100:
        return False, None, f"❌ خطأ: الدرجة {الدرجة} أكبر من 100 (الحد الأقصى هو 100)"
    
    # جميع التحققات نجحت
    return True, الدرجة, None


def طباعة_النتيجة(الدرجة, التقدير, رقم_الطالب):
    """
    دالة لطباعة نتيجة تقييم الطالب بشكل منسق
    
    المعاملات:
        الدرجة (float): درجة الطالب
        التقدير (str): تقدير الطالب
        رقم_الطالب (int): رقم الطالب في الجلسة الحالية
    """
    print("\n" + "-" * 60)
    print(f"📊 نتيجة الطالب رقم {رقم_الطالب}:")
    print(f"   🎯 الدرجة: {الدرجة}")
    
    # إضافة أيقونة مناسبة حسب التقدير
    if التقدير == "ممتاز":
        print(f"   🏆 التقدير: {التقدير} - أداء رائع!")
    elif التقدير == "جيد جداً":
        print(f"   ⭐ التقدير: {التقدير} - أداء مميز!")
    elif التقدير == "جيد":
        print(f"   ✅ التقدير: {التقدير} - أداء مقبول!")
    else:
        print(f"   📖 التقدير: {التقدير} - يحتاج إلى تحسين!")
    
    print("-" * 60)


# ============================================================================
# الدالة الرئيسية (Main Function)
# ============================================================================

def تشغيل_البرنامج():
    """
    الدالة الرئيسية لتشغيل البرنامج
    تحتوي على حلقة لا نهائية تستمر حتى يطلب المستخدم الخروج
    """
    # عرض المعلومات الأولية للمستخدم
    عرض_قائمة_التقديرات()
    عرض_التعليمات()
    
    # متغيرات للإحصائيات
    عدد_الطلاب = 0
    مجموع_الدرجات = 0
    
    # الحلقة الرئيسية للبرنامج
    while True:
        print()  # سطر فارغ لتحسين القراءة
        
        # طلب إدخال من المستخدم
        المدخلات = input("📝 الرجاء إدخال درجة الطالب (أو 'خروج' للإنهاء): ")
        
        # التحقق مما إذا كان المستخدم يريد الخروج
        if المدخلات.strip().lower() == "خروج":
            # عرض إحصائيات قبل الخروج
            print("\n" + "=" * 60)
            print("👋 شكراً لاستخدام برنامج تقدير الطلاب!")
            print(f"📈 إحصائيات الجلسة الحالية:")
            print(f"   • عدد الطلاب الذين تم تقييمهم: {عدد_الطلاب}")
            if عدد_الطلاب > 0:
                المتوسط = مجموع_الدرجات / عدد_الطلاب
                print(f"   • متوسط الدرجات: {المتوسط:.2f}")
            print("=" * 60)
            break  # الخروج من الحلقة وإنهاء البرنامج
        
        # التحقق من صحة الدرجة المدخلة
        هل_صحيح, الدرجة, رسالة_الخطأ = التحقق_من_صحة_الدرجة(المدخلات)
        
        # إذا كان الإدخال غير صحيح، عرض رسالة الخطأ والمطالبة بإعادة المحاولة
        if not هل_صحيح:
            print(رسالة_الخطأ)
            continue  # العودة إلى بداية الحلقة لطلب إدخال جديد
        
        # الإدخال صحيح، قم بحساب التقدير
        التقدير = تقدير_الدرجة(الدرجة)
        
        # تحديث الإحصائيات
        عدد_الطلاب += 1
        مجموع_الدرجات += الدرجة
        
        # عرض النتيجة للمستخدم
        طباعة_النتيجة(الدرجة, التقدير, عدد_الطلاب)


# ============================================================================
# نقطة بداية البرنامج (Program Entry Point)
# ============================================================================

if __name__ == "__main__":
    """
    هذا الكود يتم تنفيذه فقط عند تشغيل الملف مباشرة
    وليس عند استيراده كوحدة برمجية
    """
    try:
        # تشغيل البرنامج الرئيسي
        تشغيل_البرنامج()
        
    except KeyboardInterrupt:
        # معالجة حالة الضغط على Ctrl+C من قبل المستخدم
        print("\n\n⚠️ تم إيقاف البرنامج بواسطة المستخدم (Ctrl+C)")
        print("👋 إلى اللقاء!")
        
    except Exception as خطأ_غير_متوقع:
        # معالجة أي أخطاء غير متوقعة قد تحدث
        print(f"\n💥 حدث خطأ غير متوقع: {خطأ_غير_متوقع}")
        print("📞 الرجاء الاتصال بمطور البرنامج أو إعادة تشغيله")
        
    finally:
        # هذا الكود يتم تنفيذه دائماً عند انتهاء البرنامج
        print("\n" + "=" * 60)
        print("🏁 انتهى البرنامج")
        print("=" * 60)
